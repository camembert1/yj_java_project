package com.kh.day04.loop;

import java.util.Scanner;

public class Exam_DoWhile {

	public static void main(String[] args) {

		//입력한 수가 -1이 될 때까지 ~~하시오.
		
		Scanner sc = new Scanner(System.in);
		
		int num = 0;
		int sum = 1;
		do {
			System.out.print("정수 입력 : ");
			num = sc.nextInt();
			sum+=num;
			
		} while (num != -1);
		
		System.out.printf("입력한 수의 합은 %d입니다.", sum);
		
	}

}
